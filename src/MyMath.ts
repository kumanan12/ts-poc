export class MyMath {
 static add(n1, n2: number){
    return n1 + n2;
 }

 static multiply(n1: number, n2: number) {
    return n1 * n2;
 }

 static divide(n1: number, n2: number) {
    return n1 / n2;
 }

 static SQRT(n1: number) {
    return Math.sqrt(n1)
 }

 static cubed(n1: number) {
    return Math.pow(n1, 3);
 }


 
}